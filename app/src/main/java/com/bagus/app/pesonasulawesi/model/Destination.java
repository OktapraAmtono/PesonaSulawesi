public class Destination {

    private String namaDestination;
    private String kotaDestination;
    private String keteranganDestination;


    public String getNamaDestination() {
        return namaDestination;
    }

    public void setNamaDestination(String namaDestination) {
        this.namaDestination = namaDestination;
    }

    public String getKotaDestination() {
        return kotaDestination;
    }

    public void setKotaDestination(String kotaDestination) {
        this.kotaDestination = kotaDestination;
    }

    public String getKeteranganDestination() {
        return keteranganDestination;
    }

    public void setKeteranganDestination(String keteranganDestination) {
        this.keteranganDestination = keteranganDestination;
    }
}
