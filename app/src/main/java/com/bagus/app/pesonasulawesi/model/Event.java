public class Event {

    private String namaDestinationEvent;
    private String kotaDestinationEvent;
    private String keteranganDestinationEvent;


    public String getNamaDestinationEvent() {
        return namaDestinationEvent;
    }

    public void setNamaDestinationEvent(String namaDestinationEvent) {
        this.namaDestinationEvent = namaDestinationEvent;
    }

    public String getKotaDestinationEvent() {
        return kotaDestinationEvent;
    }

    public void setKotaDestinationEvent(String kotaDestinationEvent) {
        this.kotaDestinationEvent = kotaDestinationEvent;
    }

    public String getKeteranganDestinationEvent() {
        return keteranganDestinationEvent;
    }

    public void setKeteranganDestinationEvent(String keteranganDestinationEvent) {
        this.keteranganDestinationEvent = keteranganDestinationEvent;
    }

}
